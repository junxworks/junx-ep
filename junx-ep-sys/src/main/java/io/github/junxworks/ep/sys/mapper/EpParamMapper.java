package io.github.junxworks.ep.sys.mapper;

import org.apache.ibatis.annotations.Mapper;
import org.apache.ibatis.annotations.Param;
import org.apache.ibatis.annotations.Select;
import org.apache.ibatis.annotations.Update;

import io.github.junxworks.ep.core.orm.BaseMapper;
import io.github.junxworks.ep.sys.entity.EpSParam;

/**
 * <p>Mapper</p>
 * <p>Table: ep_s_dict</p>
 *
 * @since 2019-6-28 15:44:30 Generated by JunxPlugin
 */
@Mapper
public interface EpParamMapper extends BaseMapper {
	@Select("select * from ep_s_param where status=0 and param_name=#{paramName} and param_group=#{group}")
	EpSParam getParam(@Param("group") String group, @Param("paramName") String paramName);

	@Update("update ep_s_param set param_value=#{paramValue} where status=0 and param_name=#{paramName} and param_group=#{group}")
	int updateParam(@Param("group") String group, @Param("paramName") String paramName, @Param("paramValue") String paramValue);
}